export function Ja() {
  return (
    <p>
      この作品では、さまざまな環境に生きる鳥類の個性そして音楽性豊かなその鳴き声を自分なりに解釈し分析した上でそれらを記号として共通化し、新たな楽譜的な概念として再現するという挑戦を行いました。
      <br />
      <br />
      鳥の鳴き声をモチーフやテーマとして再現した作品はアートやデザイン、音楽など幅広く、現在に亘り数多のアーティストによって実験・制作されてきています。その中でアプローチの仕方として音高やリズムなどではなく、今回私は鳥の鳴き声の魅力の一つ、「音色」に焦点を当てています。
      <br />
      <br />
      当展示では数十種にも及ぶ鳥の鳴き声を学名の順番に記したものと、それらが集まり３つのテーマに応じて合奏を繰り広げる様子がご覧になれます。
      <br />
      鳥の鳴き声は言語として研究する面白さもあるが、音楽的な側面で分析しても面白い結果が得られるのではないか。Bird-Transcriptionでは、その後者を選んだ形で私自身の耳で感じたままの印象を画面に落とし込んでいきました。
    </p>
  );
}
